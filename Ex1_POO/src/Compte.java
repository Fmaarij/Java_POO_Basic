public class Compte {
    String login, motDePasse;

    //constructor
    public Compte() {

    }

    //Getter & setter
    public String getLogin() {
        return  login;
    }

    public void setLogin(String login) {
        this.login =  login;
    }

    public String getMotDePasse() {
        return motDePasse;
    }

    public void setMotDePasse(String motDePasse) {
        this.motDePasse = motDePasse;
    }

}
